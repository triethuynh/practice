public class IslandPerimeter {

	/**
	 * 463. Island Perimeter
	 * See https://leetcode.com/problems/island-perimeter/ 
	 * @param grid - the two dimension array where 1 represent the island and 0 represent the water
	 * @return the perimeter of the island
	 */
    public int islandPerimeter(int[][] grid) {
       int perimeter = 0;
       if (grid == null) {
    	   return perimeter;
       }
       
       if (grid[0].length == 0) {
    	   return perimeter;
       }
       
       
       return perimeter;
    }
    
    
}
